print('Hello world!')
# String untuk menyimpan teks, bisa dengan petik 1 atau 2 dan hanya bisa menyimpan teks
nama = 'Python'
bahasa = "Indonesia"
print(nama)

# Integer digunakan untuk menyimpan nilai bilangan bulat
umur = 20

# Float digunakan untuk angka desimal
berat = 55.5

# Boolean menyimpan dua nilai: True atau False
jika_siswa = True
jika_guru = False

# List digunakan untuk menyimpan banyak nilai dalam suatu variabel
buah = ['mangga', 'apel', 'pisang']
print(buah)

# Dictionary yang menyimpan pasangan nilai dalam bentuk key and value
siswa = {'nama': 'Python', 'umur': 16}
print(siswa)

# Penjumlahan
a = 10
b = 5
hasil_penjumlahan = a + b
print(hasil_penjumlahan)

# Pengurangan
hasil_pengurangan = a - b
print(hasil_pengurangan)

# Perkalian
hasil_perkalian = a * b
print(hasil_perkalian)

# Pembagian
hasil_pembagian = a / b
print(hasil_pembagian)

# Variabel baru
x = 10 
y = 3 

# Pembagian bulat
hasil_pembagian_bulat = x // y
print(hasil_pembagian_bulat)

# Modulus (sisa pembagian)
hasil_modulus = a % b
print(hasil_modulus)

# Mengubah isi variabel
a = 2
b = 3

# Pangkat
hasil_pangkat = a ** b
print(hasil_pangkat)

# Operator gabungan
a = 10
a += 5  # a menjadi 15
print(f'Nilai a setelah ditambah 5: {a}')

a -= 3  # a menjadi 12
print(f'Nilai a setelah dikurangi 3: {a}')

a *= 2  # a menjadi 24
print(f'Nilai a setelah dikali 2: {a}')

a /= 4  # a menjadi 6.0
print(f'Nilai a setelah dibagi 4: {a}')
